package com.FullEx;

public class SwapNum {
    public static void main(String[] args) {
        int num1,num2,x;
        num1=120;
        num2=60;
        System.out.println("Before swapping:num1,num2="+num1+","+ +num2);

        x=num1;
        num1=num2;
        num2=x;
        System.out.println("after swapping:num1,num2="+num1+","+ +num2);








    }
}
